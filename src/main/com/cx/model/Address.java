package com.cx.model;

public class Address {

	private long userid;
	private String addressType;    // '1' for business, '2' for private
	private String street;
	private String zip;
	private String city;
	private String country;
	private String state;
	private String telCountry;
	private String telCity;
	private String telNumber;
	private String faxCountry;
	private String faxCity;
	private String faxNumber;
	private String mobileCountry;
	private String mobileCity;
	private String mobileNumber;
	public long getUserid() {
		return userid;
	}
	public void setUserid(long userid) {
		this.userid = userid;
	}
	public String getAddressType() {
		return addressType;
	}
	public void setAddressType(String addressType) {
		this.addressType = addressType;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getTelCountry() {
		return telCountry;
	}
	public void setTelCountry(String telCountry) {
		this.telCountry = telCountry;
	}
	public String getTelCity() {
		return telCity;
	}
	public void setTelCity(String telCity) {
		this.telCity = telCity;
	}
	public String getTelNumber() {
		return telNumber;
	}
	public void setTelNumber(String telNumber) {
		this.telNumber = telNumber;
	}
	public String getFaxCountry() {
		return faxCountry;
	}
	public void setFaxCountry(String faxCountry) {
		this.faxCountry = faxCountry;
	}
	public String getFaxCity() {
		return faxCity;
	}
	public void setFaxCity(String faxCity) {
		this.faxCity = faxCity;
	}
	public String getFaxNumber() {
		return faxNumber;
	}
	public void setFaxNumber(String faxNumber) {
		this.faxNumber = faxNumber;
	}
	public String getMobileCountry() {
		return mobileCountry;
	}
	public void setMobileCountry(String mobileCountry) {
		this.mobileCountry = mobileCountry;
	}
	public String getMobileCity() {
		return mobileCity;
	}
	public void setMobileCity(String mobileCity) {
		this.mobileCity = mobileCity;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	
	
}
